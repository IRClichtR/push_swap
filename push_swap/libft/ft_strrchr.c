#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	int		len;
	char	*str;

	str = (char *)s;
	len = (int)ft_strlen(str);
	i = len;
	if (c == 0)
		return (str + len);
	while (i >= 0)
	{
		if (str[i] == (char)c)
			return (&str[i]);
		i--;
	}
	return (NULL);
}
